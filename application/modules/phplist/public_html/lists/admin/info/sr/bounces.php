<p>Ovo je lista vraćenih poruka. Najčešče će biti identifikovane po korisniku i po poruci.
Ako je ovo produkcioni sistem, nemojte brisati vraćene poruke, zato što sistem neće moći
da identifikuje uzastopna vraćanja, pošto se više poruka vrati. Trenutno možete samo pregledati
vraćenu poruku, ne postoji način (za sad) da se preinači sistemska identifikacija vraćene poruke.
U nekim slučajevima ovo može značiti da je nešto označeno kao vraćena poruka, iako je u pitanju
"Puno sanduče" privremena greška.</p>
