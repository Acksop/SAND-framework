Assign Invalid will be used to create an email for subscribers with an invalid email address.
You can use values between [ and ] to make up a value for the email.
For example if your import file contains a column First Name and one called Last Name, you can use [first name] [last name] to construct a new value for the email for this subscriber containing their first name and last name.
The value [number] can be used to insert the sequence number for importing.
