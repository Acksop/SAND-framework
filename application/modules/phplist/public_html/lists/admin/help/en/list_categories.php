<p>To completely remove a category from your system you will need to:</p>

<ol>
<li>Click the "x" sign in the "List Categories" page </li>
<li>Click "Save Changes" </li>

<p>Once you do this you will no longer be able to assign any other lists to that category you just removed but, to un-assign any list from it you need one additional step:</p>
<li>Click "Re-edit all lists" and "Save"</li>
</ol>

