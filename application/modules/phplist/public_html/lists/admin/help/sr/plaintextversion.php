
<h3>Verzija u formatu običnog teksta</h3>

<p>Svaki email, čak i kad je sadržaj u HTML-u, mora da ima verziju u formatu običnog teksta. Ovo se primenjuje po internet standardima i phpList to ne može da izmeni.
Ako ne popunite ovo, phpList će automatski generisati tekstualni sadržaj iz vašeg HTML-a. Alternativno, možete ovde sami upisati tekstualni sadržaj vaše kampanje.</p>

<h3>Ko će sve videti tekstualnu verziju? </h3>

<p>Tekstualna verzija će biti prikazana za dva tipa pretplatnika</p>

<ol>
<li>Pretplatnicima koji su u phpList opcijama podesili da primaju email u tekstualnom formatu. <br/>
<i>Ovim pretplatnicima će biti poslata samo tekstualna verzija.</i></li>
<li>Pretplatnicima koji koriste email čitače koji ne mogu prikazati HTML verziju. <br/>
Neki pretplatnici koriste programe koji ne mogu rukovati sa HTML-om za čitanje email-a. U tom slučaju, oni će videti samo tekstualnu verziju. Ovo se odlučuje sa pretplatnikove strane komunikacionog kanala.
<i>Ovi pretplatnici će primiti normalan email koji sadrži i HTML i tekstualnu verziju, ali će njihov sistem odlučiti da prikaže samo tekstualnu verziju.</i>
</li>
</ol>

