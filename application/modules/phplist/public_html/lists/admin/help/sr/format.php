<h3>Format poruke</h3>
Ako koristite "automatsko otkrivanje", poruka će biti klasifikovana kao HTML čim je nađen jedan HTML tag (&lt; ... &gt;).
</p><p><b>Možete bezbedno ostaviti označeno "Automatski otkrij"</b></p><p>
Ako niste sigurni da li "automatsko otkrivanje" radi i ako je poruka koju kopirate formatirana kao HTML, izaberite "HTML".
Spoljne reference ka resursima (npr. slikama) će morati da imaju kompletan URL, tj. da započinju sa http:// (za razliku od slika u obrascima).
Za sve ostalo, vi ste u potpunosti odgovorni za formatiranje teksta.
<p>Ako želite da forsirate da poruka bude običan tekst, označite "Tekst".
</p><p>
Ova informacija se koristi da se napravi tekstualna verzija poruke u HTML formatu ili HTML verzija poruke u tekstualnom formatu.
Ovo formatiranje će biti po sledećem:<br/>
Original je HTML -&gt; tekst<br/>
<ul>
<li><b>Podebljan</b> tekst će biti ograđen <b>*-znakovima</b>, <b>iskošen</b> tekst sa <b>/-znakovima</b></li>
<li>Linkovi u tekstu će biti zamenjeni sa tekstom, praćenim sa URL-om u zagradama</li>
<li>Veliki delovi teksta će biti zalomljeni posle 70-tog znaka</li>
</ul>
Original je Tekst -&gt; HTML<br/>
<ul>
<li>Dva znaka za novi red će biti zamenjeni sa &lt;p&gt; (paragraf)</li>
<li>Jedan znak za novi red će biti zamenjen sa &lt;br /&gt; (prekid reda)</li>
<li>Email adrese će moći da se kliknu</li>
<li>URL-ovi će moći da se kliknu. URL-ovi mogu biti u bilo kojem od sledećih formata:<br/>
<ul><li>http://some.website.url/some/path/somefile.html
<li>www.websiteurl.com
</ul>
Napravljeni linkovi će imati klasni stil "url" i cilj će im biti "_blank".
</ul>
<b>Upozorenje</b>: odabirom da je vaša poruka tekst, a kopiranje HTML teksta u polje će učiniti da se HTML tekst kod pošalje korisnicima koji žele da primaju HTML email-ove.
